package com.juankysoriano.materiallife.ui.sketch.menu;

public class NormalisedCenter {
    private final float x;
    private final float y;

    public NormalisedCenter(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
